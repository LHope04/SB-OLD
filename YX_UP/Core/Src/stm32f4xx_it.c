/**
  * @brief This function handles TIM1 update interrupt and TIM10 global interrupt.
  */
void TIM1_UP_TIM10_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 0 */
	
	//注：每0.01s进一次中断
	if (__HAL_TIM_GET_FLAG(&htim1, TIM_FLAG_CC1) != RESET)
	{
		//锁车计时
		if(Sentry.foe_flag)	//识别到目标
		{
			Sentry.foe_count++;
			if(Sentry.foe_count == Lock_time * 100)		//视觉暂留，一直能识别到会一直刷新这个计数值
			{
				Sentry.foe_flag = 0;	//暂留结束重新赋0
			}
		}
		
//		//拨盘堵转
//		if(abs(motor_info[4].rotor_speed) < (19*5))	//检测下拨盘是否堵转
//		{
//			bopan_count++;
//		}
//		else
//		{
//			bopan_count = 0;
//		}
//		
//		if(bopan_count == 20)
//		{
//			bopan_fan_count = 7;
//			bopan_count = 0;
//		}
//		
//		if(bopan_fan_count)
//		{
//			bopan_fan_count--;
//			bopan_reversal_flag = 1;
//		}
//		else
//		{
//			bopan_reversal_flag = 0;
//		}
		
		//巡逻定时计时
		TIM1_Count++;
		if(TIM1_Count == Research_time * 100)		//俯仰切换定时
		{
			TIM1_Count =0;
			if(TIM1_Mode == 1)
			{
				TIM1_Mode = 2;
			}
			else if(TIM1_Mode == 2)
			{
				TIM1_Mode = 1;
			}
		}
	}
  /* USER CODE END TIM1_UP_TIM10_IRQn 0 */
  HAL_TIM_IRQHandler(&htim1);
  HAL_TIM_IRQHandler(&htim10);
  /* USER CODE BEGIN TIM1_UP_TIM10_IRQn 1 */

  /* USER CODE END TIM1_UP_TIM10_IRQn 1 */
}

